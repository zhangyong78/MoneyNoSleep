from __future__ import annotations

import streamlit as st


st.set_page_config(page_title="条件特征说明", layout="wide")

st.title("条件特征说明")
st.caption("这页给复盘工作人员说明条件含义、日期可用性，以及特征库如何服务历史回放。")

st.subheader("先记住一条原则")
st.info("所有条件都按 trade_date 生效。模拟 2025-12-17 选股时，系统只能使用截至 2025-12-17 收盘后已经可见的数据。")

st.subheader("数据分两层")
st.markdown(
    """
    - `stock_daily_features`：每只股票、每个交易日一行，存当天收盘后可确定的状态，例如涨停标记、上下影线比例、均线突破、距上次突破天数。
    - `stock_daily_followups`：针对历史事件的后续表现，按锚点日期存储，并带有 `available_date_5d`。只有到了这个可用日期之后，这条后续表现才能被条件选股使用。
    """
)

st.subheader("当前可用条件")
st.markdown(
    """
    - 窗口内有一天成交额达标：例如近 10 天内有一天成交额超过 10 亿。
    - 涨停次数：例如近 30 天涨停次数不少于 2 次。
    - 上影线次数：例如近 30 天上影线超过 5% 的天数不少于 3 次。
    - 下影线次数：例如近 30 天下影线超过 4% 的天数不少于 2 次。
    - 成交额事件后表现：例如近 30 天内，存在一天成交额超过 10 亿，且其后 5 日成交额总和超过 50 亿。
    - 均线突破时序：例如近 10 天突破 MA20，且近 5 天突破 MA55。
    """
)

st.subheader("复盘时怎么理解")
st.markdown(
    """
    - 当天特征：涨停、影线、成交额、换手率、均线突破，都属于当天收盘后可确认的数据。
    - 历史窗口统计：近 10 天、近 30 天这类条件，都是从对应 `trade_date` 往前回看。
    - 后续表现条件：只有历史上已经走完后 5 日的事件，才会在未来某个 `trade_date` 被当成可用条件。
    """
)

st.subheader("几个例子")
st.markdown(
    """
    1. 模拟 `2025-12-17` 选股时，近 30 天涨停次数，只统计 `2025-12-17` 及之前的交易日。
    2. 模拟 `2025-12-17` 选股时，如果某只股票在 `2025-12-15` 才出现成交额超过 10 亿，那么“其后 5 日成交额总和”还没有成熟，不能参与当日筛选。
    3. 模拟 `2025-12-17` 选股时，如果某只股票在 `2025-12-08` 出现成交额超过 10 亿，到 `2025-12-17` 已经走完后 5 个交易日，那么这条历史事件的后续表现可以参与筛选。
    """
)

st.subheader("给复盘同事的使用建议")
st.markdown(
    """
    - 先确定你是在找“当天状态”，还是“过去一段时间的历史行为”。
    - 只要条件里出现“近 N 天”“次数”“后 5 日”这类字眼，就要按指定日期理解，不能按今天的视角理解。
    - 同一条条件既可以用于当天选股，也可以用于历史溯源；关键区别只是 `trade_date` 不同。
    """
)
