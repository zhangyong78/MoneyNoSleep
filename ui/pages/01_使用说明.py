from __future__ import annotations

import streamlit as st


st.set_page_config(page_title="使用说明", layout="wide")

st.title("使用说明")
st.caption("这一页说明左侧菜单分别做什么，以及推荐的使用顺序。")

st.subheader("页面说明")
st.markdown(
    """
    - `主工作台`：总览、结果查看，以及同步和复盘的主要操作入口。
    - `使用说明`：查看菜单解释、启动方式、推荐顺序。
    - `数据同步`：说明如何把 miniQMT、BaoStock 行情同步到本地数据库。
    - `条件选股`：执行单日筛选、多组条件筛选、历史溯源。
    - `连续复盘`：查看日线复盘和 5 分钟回踩复盘的用途。
    - `交易复核`：查看买卖点、人工复核说明。
    - `问题归因`：汇总人工复核后的问题标签统计。
    - `自选观察`：后续扩展的观察池入口。
    """
)

st.subheader("推荐使用顺序")
st.markdown(
    """
    1. 先打开 `主工作台`
    2. 在侧边栏做 `miniQMT 连接测试`
    3. 运行 `miniQMT 行情同步` 或 `BaoStock 行情同步`
    4. 到 `条件选股` 页面执行筛选
    5. 回到 `主工作台` 运行快速复盘或 5 分钟回踩复盘
    6. 查看交易结果，再进入 `交易复核` / `问题归因`
    """
)

st.subheader("启动方式")
st.markdown(
    """
    - 双击项目根目录里的 `启动Moneynosleep.cmd`
    - 或命令行运行 `mns start-ui`
    - 默认地址一般是 `http://127.0.0.1:8501`
    - 如果 `8501` 被占用，系统会自动切换到下一个可用端口
    """
)

st.info("当前版本重点是本地行情同步、选股、复盘、人工复核。真实自动下单还没有开放。")
